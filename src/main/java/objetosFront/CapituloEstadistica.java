package objetosFront;

public class CapituloEstadistica {

	public String titulo;
	public String serie;
	public int cantidadResenas;
	
	
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getSerie() {
		return serie;
	}
	public void setSerie(String serie) {
		this.serie = serie;
	}
	public int getCantidadResenas() {
		return cantidadResenas;
	}
	public void setCantidadResenas(int cantidadResenas) {
		this.cantidadResenas = cantidadResenas;
	}
	
	
	
}
