package objetosFront;

public class CapituloEstadisticaEdad {
	
	
    private int id;
    private String titulo;
    private String serie;
    private int cantidadResenas;
    private double edadPromedio;
    
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String capitulo) {
		this.titulo = capitulo;
	}
	public String getSerie() {
		return serie;
	}
	public void setSerie(String serie) {
		this.serie = serie;
	}
	public int getCantidadResenas() {
		return cantidadResenas;
	}
	public void setCantidadResenas(int cantidadResenas) {
		this.cantidadResenas = cantidadResenas;
	}
	public double getEdadPromedio() {
		return edadPromedio;
	}
	public void setEdadPromedio(double edadPromedio) {
		this.edadPromedio = edadPromedio;
	}

    
}